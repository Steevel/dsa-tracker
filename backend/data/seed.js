const seedDB = async () => {
  try {
    await mongoose.connect(process.env.MONGODB_URI);
    console.log("✅ Connected to MongoDB");

    await Topic.deleteMany({});
    console.log("🗑️  Cleared existing topics");

    await Topic.insertMany(seedData);
    console.log(`✅ Seeded ${seedData.length} topics with problems`);

    const totalProblems = seedData.reduce((sum, t) => sum + t.problems.length, 0);
    console.log(`📊 Total problems seeded: ${totalProblems}`);

    mongoose.connection.close();
    console.log("✅ Database connection closed");
  } catch (error) {
    console.error("❌ Seed error:", error);
    process.exit(1);
  }
};

seedDB();
